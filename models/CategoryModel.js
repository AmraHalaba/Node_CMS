//-------------------------------------------------------------------------------------------------------------------------------------
//IMPORTS - LIBRARIES/MODULES
const mongoose = require("mongoose");

//-------------------------------------------------------------------------------------------------------------------------------------
//CREATE VARIABLES
const Schema = mongoose.Schema;

//-------------------------------------------------------------------------------------------------------------------------------------
//DEFINE POST SCHEMA
const CategorySchema = new Schema({
  title: {
    type: String,
    required: true,
  },
});

//-------------------------------------------------------------------------------------------------------------------------------------
//EXPORTS
module.exports = mongoose.model("category", CategorySchema);

